	/**
	 * @return ffColorInfo
	 */	
	public function createUserColorLibraryItem() {
		$this->_getClassloader()->loadClass('ffColor');
		$this->_getClassloader()->loadClass('ffUserColorLibraryItem');
		
		$color = new ffColor();
		
		$color->setHex('#ffffff');
		
		$colorLibraryItem = new ffUserColorLibraryItem($color);
		
		$colorLibraryItem->setTimestamp( time() );
		
		return $colorLibraryItem;
	}
